create database GESTION_CITA
go

use GESTION_CITA
go

create table TipoAtencion(
	IdTipoAtencion numeric(8) not null,
	TipoAtencion varchar (40) not null,
	constraint PK_TipoAtencion primary key (IdTipoAtencion)
)
go

create table EstadoCita(
	IdEstadoCita numeric(1) not null,
	EstadoCita varchar (25) not null,
	constraint PK_EstadoCita primary key (IdEstadoCita)
)
go                                                                

create table Especialidad
( 
	CodEspecia numeric(8) not null,
	Especialidad varchar(100) not null,
	constraint PK_Especialidad primary key (CodEspecia)
)
go

create table Consultorio
( 
	CodConst numeric(8) not null,
	NomConst varchar(50) not null,
	CodEspecia numeric(8) not null,
	constraint PK_Consultorio primary key (CodConst),
	constraint FK_Especialidad_Consultorio foreign key (CodEspecia) references Especialidad(CodEspecia)
)
go

create table Recepcionista
( 
	DniRecep int not null,
	NomRecep varchar(50) not null,
	ApellRecep varchar(50) not null,
	TelfRecep numeric(9) not null,

	Contrasena varchar(20) not null,
	EsAdmin bit not null,

	constraint PK_Recepcionista primary key (DniRecep)
)
go



create table Horario
( 
	CodHorario int not null,
	Dia varchar(20) not null,
	HoraInicio time not null,
	HoraFin time not null,
	LimitPct int not null,
	EstadoHorario bit not null,
	constraint PK_Horario primary key (CodHorario),
)
go

create table Paciente
( 
	DniPct int not null,
	NomPct varchar(50) not null,
	ApellPct varchar(50) not null,
	TlfPct numeric(9) not null,
	GeneroPct bit not null,
	EmailPct varchar(100) null,
	FechNaciPct datetime not null,
	DirecPct varchar(50) not null,
	OcupPct varchar(120) null,
	GrupSangPct int not null,
	ProcedenciaPct varchar(80) not null,
	EstCivilPct int not null,
	GrupEtnicoPct varchar(80) null,
	CentrTrabPct varchar(80) null,
	GradInstrPct int not null,
	HijosPct int not null,
	constraint PK_Paciente primary key (DniPct)
)
go

create table Doctor
( 
	DniDoc int not null,
	CodConst numeric(8) null,
	NomDoc varchar(50) not null,
	ApellDoc varchar (50) not null,
	CorreoDoctor varchar(100) not null,
	TelfDoctor numeric(9) not null,
	constraint PK_Doctor primary key (DniDoc),
	constraint FK_Consultorio_Doctor foreign key (CodConst) references Consultorio(CodConst)
)
go

create table Doctor_Horario
( 
	DniDoc int not null,
	CodHorario int not null,
	constraint PK_Doctor_Horario primary key (DniDoc, CodHorario),
	constraint FK_Doctor_Doctor_Horario foreign key (DniDoc) references Doctor(DniDoc),
	constraint FK_Horario_Doctor_Horario foreign key (CodHorario) references Horario(CodHorario)
)
go

create table Especialidad_Doctor
( 
	CodEspecia numeric(8) not null,
	DniDoc int not null,
	constraint PK_Especialidad_Doctor primary key (CodEspecia, DniDoc),
	constraint FK_Especialidad_Especialidad_Doctor foreign key (CodEspecia) references Especialidad(CodEspecia),
	constraint FK_Doctor_Especialidad_Doctor foreign key (DniDoc) references Doctor(DniDoc)
)
go

create table Cita
( 
	CodCita int not null,
	DniRecep int not null,
	DniPct int not null,
	DniDoc int not null,
	CodHorario int not null,	
	HoraInicio time null,
	HoraFin time null,
	FechaCita date not null,
	IdEstadoCita numeric(1) not null,
	IdTipoAtencion numeric(8) not null,
	FechaReprogra date null,
	FechaAnulacion date null,
	constraint PK_Cita primary key (CodCita),
	constraint FK_Horario_Cita foreign key (CodHorario) references Horario(CodHorario),
	constraint FK_Recepcionista_Cita foreign key (DniRecep) references Recepcionista(DniRecep),
	constraint FK_Paciente_Cita foreign key (DniPct) references Paciente(DniPct),
	constraint FK_Doctor_Cita foreign key (DniDoc) references Doctor(DniDoc),
	constraint FK_EstadoCita_Cita foreign key (IdEstadoCita) references EstadoCita(IdEstadoCita),
	constraint FK_TipoAtencion_Cita foreign key (IdTipoAtencion) references TipoAtencion(IdTipoAtencion)
)
go

